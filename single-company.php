                                                                        <div class="field field--name-field-employee-answer field--type-string-long field--label-hidden field__item">In order to understand how Formlabs has revolutionized the 3D printing industry, you really have to go back to 2011 and survey the landscape. The 3D printing craze was in full swing and the market became flooded with low-cost FDM machines. These consumer-level printers were great for small projects and hobbyists. But there really weren’t any options available for small businesses, entrepreneurs and professionals who needed high resolution, low-cost manufacturing in-house. Formlabs was the first company to bring an SLA 3D printer to people’s desktops and allow them to prototype real-world products without shelling out tens of thousands of dollars. This was a massive game changer for medical researchers, jewelers, film makers and so many other professions who were suddenly able to produce things in-house that were never before possible due to cost and quality. Starting off as an MIT afterschool project, we’ve swelled over the past few years to a now 200+ person company. I think it’s safe to say that we struck a cord in the market!</div>
